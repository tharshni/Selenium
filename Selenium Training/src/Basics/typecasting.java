package Basics;

public class typecasting {
	public static void main(String[] args) {
		int age=32;
		System.out.println(age);
		/*int to float*/
		float age1=(float)age;
		System.out.println(age1);
		
		/*float to double*/
		double age2=(double)age1;
		System.out.println(age2);
	}

}
